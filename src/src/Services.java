    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
     try{
      con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hospital_management", "root", "");
       if (txtServiceName.getText().equals("")) {
           JOptionPane.showMessageDialog( this, "Lütfen servisin Adı Giriniz","Hata", JOptionPane.ERROR_MESSAGE);
           return;
            }
        if (txtServiceDate.getText().equals("")) {
           JOptionPane.showMessageDialog( this, "Lütfen servisin Tarihi Giriniz","Hata", JOptionPane.ERROR_MESSAGE);
           return;
            }
                if (txtPatientID.getText().equals("")) {
           JOptionPane.showMessageDialog( this, "Lütfen Hastanın ID si Giriniz","Hata", JOptionPane.ERROR_MESSAGE);
           return;
            }
            if (txtServiceCharges.getText().equals("")) {
           JOptionPane.showMessageDialog( this, "Lütfen servis Ücreti Giriniz","Hata", JOptionPane.ERROR_MESSAGE);
           return;
            }
      
       String sql= "insert into Services(ServiceName,ServiceDate,PatientID,ServiceCharges)values('"+ txtServiceName.getText() + "','"+ txtServiceDate.getText() + "','" + txtPatientID.getText() + "'," + txtServiceCharges.getText() + ")";
       pst=con.prepareStatement(sql);
       pst.execute();
     
      JOptionPane.showMessageDialog(this,"Başarıyla Kaydedildi","",JOptionPane.INFORMATION_MESSAGE); 
      btnSave.setEnabled(false);
       }catch(HeadlessException | SQLException ex){
           JOptionPane.showMessageDialog(this,ex); 
                }
    }//GEN-LAST:event_btnSaveActionPerformed

